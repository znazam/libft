#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int I;

	i = 0;
	I = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[I] != '\0')
	{
		s1[i + I] = s2[I];
		I++;
	}
	s1[i + I] = '\0';
	return (s1);
}
